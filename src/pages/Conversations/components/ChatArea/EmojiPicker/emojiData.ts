// Utilitários para gerenciar emojis recentes
export const getRecentEmojis = (): string[] => {
  try {
    const recent = localStorage.getItem('recentEmojis');
    return recent ? JSON.parse(recent) : [];
  } catch {
    return [];
  }
};

export const addRecentEmoji = (emoji: string): void => {
  try {
    let recent = getRecentEmojis();
    // Remove se já existe
    recent = recent.filter(e => e !== emoji);
    // Adiciona no início
    recent.unshift(emoji);
    // Limita a 21 emojis (3 linhas de 7)
    recent = recent.slice(0, 21);
    localStorage.setItem('recentEmojis', JSON.stringify(recent));
  } catch {
    // Falha silenciosa se localStorage não estiver disponível
  }
};
